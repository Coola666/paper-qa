# 2. Markdown报告生成（最终版）
# =========================
def generate_markdown_report(answer, analysis_text, sota_analysis, table_summary):
    now = datetime.now().strftime("%Y-%m-%d %H:%M:%S")

    report = f"""
# 📄 Research Paper AI Report

Generated Time: {now}

---

## 🧠 1. Question Answering Result

{answer if answer else "No QA result generated"}

---

## 📊 2. Paper Analysis (Abstract / Contribution / Conclusion)

{analysis_text if analysis_text else "No analysis generated"}

---

## 🏆 3. SOTA vs Baseline Analysis

{sota_analysis if sota_analysis else "No SOTA analysis generated"}

---

## 📈 4. Extracted Experimental Tables

{table_summary if table_summary else "No tables extracted"}

---

## 🧾 5. Final Conclusion

This report is automatically generated by a Retrieval-Augmented Research Paper AI Agent.

It integrates:
- RAG-based question answering
- LLM-based paper understanding
- Experimental result extraction
- SOTA comparison analysis
"""

    return report